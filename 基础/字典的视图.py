s = {'张星':100, 'James':101, '尅尅':99}

# 获取字典的key
keys = s.keys()
print(keys)
print(type(keys))
print(list(keys))

# 获取字典里的value
values = s.values()
print(values)
print(type(values))
print(list(values))


items = s.items()
print(items)
print(type(items))
print(list(items))